"""
Imagina que tienes una variable puntaje que inicia en 100. Mediante operadores de asignación compuesta, realiza lo siguiente en orden: asígnale el valor inicial, súmale 20, réstale 10, multiplícalo por 2 y finalmente divídelo entre 5. Muestra el resultado tras cada operación.
"""

# Asignación inicial
puntaje = 100 
print(f"Valor Inicial: {puntaje}")

# Asignación con suma
puntaje += 20    # 120
print(f"Resultado Suma: {puntaje}")
# Asignación con resta
puntaje -= 10    # 110
print(f"Resultado Resta: {puntaje}")
# Asignación con multiplicación
puntaje *= 2     # 220
print(f"Resultado Multiplicacion: {puntaje}")
# Asignación con división
puntaje /= 5     # 44.0
print(f"Resultado Division: {puntaje}")
