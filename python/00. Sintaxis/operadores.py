# Operadores de asignacion
numero = 15
# Sumar y asignar 5
numero += 5 # 20
# Restar y asignar 10
numero -= 10 # 10
# dividir y asignar
numero /= 2 # 5
# multiplicar y asignar
numero *= 20 # 100

print(numero)

# Operadores de comparacion
a = 10
b = 5
# Igualdad
print(f"Igualdad: {a == b}")
# No Igual
print(f"No Igual: {a != b}")



# COMENTARIOS DENTRO DE PRINT
print("Valor de a: " , a, "Valor de b: ", b)
print(f"Valor de a: {a} Valor de b: {b}")

